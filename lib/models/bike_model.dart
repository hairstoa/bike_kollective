import 'package:flutter/material.dart';

class Bike {
  final double id;
  final String name;
  final String type;
  final double height;
  final double age;
  final String rental;
  final String image;

  Bike({
    required this.id,
    required this.name,
    required this.type,
    required this.height,
    required this.age,
    required this.rental,
    required this.image,
  });
}
